#include <stdio.h>

int main(){
	int n;
	int cnt;
	//1读取一个1-9的整数
	do{
		//1.1读取一个整数
		printf("请输入一个1-9的整数");
		cnt = scanf("%d",&n);
		//1.2清楚缓冲区
		scanf("%*[^\n]%*c");
		//1.3只要输入无效或者不在1-9之间就重来（回到1.1）
	}while(cnt!=1||n<1||n>9);
	//2 以那个数为行数输出九九表
	int i=0;	
	int j=0;
	for(i=1;i<=n;i++){
		//2.1反复n次，每次输出一行
		for(j=1;j<=i;j++){
			//2.2反复根据是第几行决定输出几个乘法式
			printf("%dx%d=%d%c",
			//2.3每个乘法式是：小*大=积
			j,i,j*i,
			//2.4乘法式后面输出逗号或者换行
			j!=i?',':'\n');
		}
	}
	return 0;
}
